    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        db = DB.getObj();
        ResultSet rs = db.query("select employee_id,fname,lname,emp_phone_no,emp_address,emp_ratings from teacher where employee_id =" + jTextField1.getText());
        Teacher teacher = new Teacher();
        try {
            while (rs.next()) {
                teacher.setEmployeeId(rs.getInt(1));
                teacher.setFname(rs.getString(2));
                teacher.setLname(rs.getString(3));
                teacher.setPhoneNo(rs.getString(4));
                teacher.setAddress(rs.getString(5));
                if(rs.getString(6)!=null)
                teacher.setRating(Double.valueOf(rs.getString(6)));
            }
            if(teacher.getEmployeeId()==0)
                JOptionPane.showMessageDialog(null, "No Record with Employee Id "+jTextField1.getText());
            jTextField1.setText(String.valueOf(teacher.getEmployeeId()));
            jTextField2.setText(teacher.getFname());
            jTextField3.setText(teacher.getLname());
            jTextField4.setText(teacher.getAddress());
            jTextField5.setText(String.valueOf(teacher.getRating()));
            jTextField6.setText(teacher.getPhoneNo());

        } catch (SQLException ex) {
            System.out.println(ex);
            Logger.getLogger(TeacherUpdateDelete.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

