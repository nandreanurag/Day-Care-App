    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //update button
        try {
            if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty()
                    || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField5.getText()==null|| jTextField6.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Fields cannot be left empty");
                return;
            }
            System.out.println(jTextField5.getText());
            Teacher teacher = new Teacher();
            teacher.setEmployeeId(Integer.parseInt(jTextField1.getText()));
            teacher.setFname(jTextField2.getText());
            teacher.setLname(jTextField3.getText());
            teacher.setAddress(jTextField4.getText());
            if(jTextField5!=null)
            teacher.setRating(Double.valueOf(jTextField5.getText()));
            teacher.setPhoneNo(jTextField6.getText());
            boolean flag;

            flag = teacher.getFname().matches("^[a-zA-Z '-]+$");
            if (flag == false) {
                JOptionPane.showMessageDialog(null, "First name cannot have integer values");
                return;
            }

            flag = teacher.getLname().matches("^[a-zA-Z '-]+$");

            if (!flag) {
                JOptionPane.showMessageDialog(null, "Last name cannot have integer values");
                return;
            }
            String phoneNumber = teacher.getPhoneNo();

            if (phoneNumber.length() != 10) {
                JOptionPane.showMessageDialog(null, "Phone Number must be of 10 digits");
                return;
            }
            flag = phoneNumber.matches("^[0-9]+$");
            if (!flag) {
                JOptionPane.showMessageDialog(null, "Phone Number must have digits only");
                return;
            }
            if(teacher.getRating()==null){
                JOptionPane.showMessageDialog(null, "Rating Can't be null");
            }
//            Statement stmt = DB.getObj().conn.createStatement();
//            ResultSet rs = stmt.executeQuery("Select * From teacher");
//            while (rs.next()) {
//                if (rs.getObject("emp_phone_no").equals(phoneNumber)) {
//                    JOptionPane.showMessageDialog(null, "Phone Number already exists");
//                    return;
//                }
//            }
            db = DB.getObj();
            PreparedStatement pstmt;
            try {
                pstmt = db.conn.prepareStatement("update teacher set fname= ?, lname =? , emp_phone_no =?,emp_address=?, emp_ratings=? where employee_id = ?");
                pstmt.setString(1, teacher.getFname());
                pstmt.setString(2, teacher.getLname());
                pstmt.setString(3, teacher.getPhoneNo());
                pstmt.setString(4, teacher.getAddress());
                pstmt.setDouble(5,teacher.getRating());
                pstmt.setInt(6,teacher.getEmployeeId());
                int row = pstmt.executeUpdate();
                if(row==-1)
                    JOptionPane.showMessageDialog(null, "Update Failed Please Try Again");
                else
                    JOptionPane.showMessageDialog(null, "Sucessfully Updated");
            } catch (SQLException ex) {
                Logger.getLogger(ImmunizationRecord.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Update Failed Please Try Again");
            System.out.println("Error" + e.getMessage());
//          System.out.println(e.printStackTrace());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

